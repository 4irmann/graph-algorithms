/* Speichert Graphenparameter im Text-Exportformat des Programmes Graf v1.3h von smsoft. 
   In diesem Format koennen gerichtete, gewichtete Graphen gespeichert werden. */

public class smsoftTextGraphParameters {
	
	// Eigentliche Graphenparameter in Form eines Strings
	public String text;

	// CONSTRUCTOR: 
	public smsoftTextGraphParameters(String s) {
		text = s;
	}

}

